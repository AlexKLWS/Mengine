#	include "AOIActor.h"

namespace Menge
{
	//////////////////////////////////////////////////////////////////////////
	AOIActor::AOIActor()
		: m_provider( nullptr )
		, m_remove(false)
	{
	}
	//////////////////////////////////////////////////////////////////////////
	AOIActor::~AOIActor()
	{
	}
	//////////////////////////////////////////////////////////////////////////
	void AOIActor::setProvider( AOIActorProviderInterface * _provider )
	{
		m_provider = _provider;
	}
	//////////////////////////////////////////////////////////////////////////
	AOIActorProviderInterface * AOIActor::getProvider()
	{
		return m_provider;
	}
	//////////////////////////////////////////////////////////////////////////
	void AOIActor::remove()
	{
		m_remove = true;
	}
	//////////////////////////////////////////////////////////////////////////
	bool AOIActor::isRemoved() const
	{
		return m_remove;
	}
}